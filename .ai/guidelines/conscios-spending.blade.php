# Conscious spending plan

This project is about conscious spending plan, that was created by Ramit Sethi https://www.iwillteachyoutoberich.com/.

## Main Feature

It's a table which contain 5 categories:

 - Total Net Worth (Assets + Invested + Saving - Debt = Total Net Worth)
 - Income
    - For each partner how much their bring net and gross
    - Calculate total income combine by year.
 - Total Expenses with some fixed categories ( = Calculate percentage base total expenses with income)
    - Rent or Mortage
    - Utilities (gas, water, electrict, internet, cable, etc)
    - Insurance
    - Car Payment
    - Groceries
    - Clothes
    - Phone
    - Subscription
    - Debt
    - Buffer (Add automatically 15% on top of sum of each item above - it can be change)
 - Investing ( = Calculate percentage base total investing with income)
    - Post-tax Retirement Saving
    - ETF
    - Other
 - Saving Goal ( = Calculate percentage base total saving goal with income)
    - Vacation
    - Gifts
    - Long Term Emergency Fund
 - Guilty-free Spending ( = Calculate the rest that left)

Each category have 3 coluns, description of categories and subcategories, partner 1 information and partner 2 information.


